package com.vivo.vivorajonboarding.model;

public class ExperienceModel {
    private String id, userid, company_name, company_job_title, company_start_date, company_end_date, experience_letter, time, request, ho_request;

    public ExperienceModel() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    public String getCompany_name() {
        return company_name;
    }

    public void setCompany_name(String company_name) {
        this.company_name = company_name;
    }

    public String getCompany_job_title() {
        return company_job_title;
    }

    public void setCompany_job_title(String company_job_title) {
        this.company_job_title = company_job_title;
    }

    public String getCompany_start_date() {
        return company_start_date;
    }

    public void setCompany_start_date(String company_start_date) {
        this.company_start_date = company_start_date;
    }

    public String getCompany_end_date() {
        return company_end_date;
    }

    public void setCompany_end_date(String company_end_date) {
        this.company_end_date = company_end_date;
    }

    public String getExperience_letter() {
        return experience_letter;
    }

    public void setExperience_letter(String experience_letter) {
        this.experience_letter = experience_letter;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getRequest() {
        return request;
    }

    public void setRequest(String request) {
        this.request = request;
    }

    public String getHo_request() {
        return ho_request;
    }

    public void setHo_request(String ho_request) {
        this.ho_request = ho_request;
    }
}
